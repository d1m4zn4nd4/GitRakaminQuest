package JavaTypeData;

public class JavaTypeData {
    public static void main(String[] args) {
        // Integer data types
        int myInt = 10;
        byte myByte = 20;
        short myShort = 30;
        long myLong = 40L;

        // Floating-point data types
        float myFloat = 3.14f;
        double myDouble = 2.71828;

        // Character data type
        char myChar = 'A';

        // Boolean data type
        boolean myBoolean = true;

        // Display data types
        System.out.println("Integer Data Types:");
        System.out.println("int: " + myInt);
        System.out.println("byte: " + myByte);
        System.out.println("short: " + myShort);
        System.out.println("long: " + myLong);

        System.out.println("\nFloating-Point Data Types:");
        System.out.println("float: " + myFloat);
        System.out.println("double: " + myDouble);

        System.out.println("\nCharacter Data Type:");
        System.out.println("char: " + myChar);

        System.out.println("\nBoolean Data Type:");
        System.out.println("boolean: " + myBoolean);
    }
}
